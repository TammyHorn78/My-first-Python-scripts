radius = float(input("Enter the radius of a sphere: "))
diameter = 2 * radius
circumference = 2 * 3.14 * radius
area = 4 * 3.14 * (radius ** 2)
volume = (4 / 3) * 3.14 * (radius ** 3)
print("The diameter of the sphere is", diameter, "units.")
print("The circumference of the sphere is", circumference, "units.")
print("The surface area of the sphere is", area, "square units.")
print("The volume of the sphere is", volume, "cubed units.")
